<?php
// Получаем число k от пользователя
$k = (int)readline("Введите число k (от 1 до 300): ");

// Проверка на корректность значения k
if ($k < 1 || $k > 300) {
    echo "Число k должно быть в диапазоне от 1 до 300.";
    exit;
}

// Генерируем строку из чисел от 100 до 199
$sequence = "";
for ($i = 100; $i <= 199; $i++) {
    $sequence .= $i;
}

// Выводим k-ю цифру (нумерация с 1)
echo "k-ая цифра последовательности: " . $sequence[$k - 1];
?>

